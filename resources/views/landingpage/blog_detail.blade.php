                        <div class="article-description">
                            <p>City of matoa begitu 3 penggalan kata ini menjadi mimpi dari masyarakat Sukorejo. Karena di bawah kepemimpinan Camat Diano Vella Veri, menjadi Sukorejo Kota Matoa</p>
                            <p>Untuk mewujudkan mimpi indah itu Camat Sukorejo melakukan berbagai langkah strategis.</p>
                            <p>“Saya mengumpulkan seluruh kepala desa yang berjumlah 19 orang. Saya ajak diskusi tentang keinginan Kecamatan Sukorejo menjadikan Kota Matoa,” ujar Diano.

                                Dikatakan lagi, semua kepala desa menyambut gagasan tadi. Dan menyiapkan anggaran di APBDes masing-masing. Disepakati 200 pohon untuk masing-masing desa,  2 batang pohon untuk 1 rumah.</p>
                            <p>Program itu dimulai 2015 lalu, tidak hanya menggandeng desa,  ia pun melakukan pendekatan dengan HM Sampoerna yang lokasi pabriknya berada di Kecamatan Sukorejo.

                                Bak gayung bersambut HM Sampoerna antusias dengan mimpi itu. Sehingga HM Sampoerna memberikan bibit dan melakukan pelatihan olahan dari matoa. Total bibit saat ini yang sudah ditanam 32.000 batang.</p>
                            <p>“Lambat laun mimpi itu mulai kelihatan, karena sudah ada pohon matoa yang mulai berbuah,” tuturnya.

                                Cty of Matoa itu tidak hanya soal pohonnya saja, menurut Diano masyarakat antusias dengan mengangkat nama buah matoa, dengan batik yang motif matoa.

                                Yang membedakan dengan batik lainnya adalah pewarnaannya menggunakan bahan alam. “UKM batik kami diundang 2 kali ke Korsel karena pewarnanya yang alami,” kata Diano yang termasuk Camat Inovatif se Jatim. (aza/ian)</p>
